#include "miniRT.h"

int	ft_punctuation(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] == '.' || str[i] == ',')
		{
			if (i == 0 || ft_isspace(str[i - 1]) || ft_isspace(str[i + 1]))
				return (1);
		}
		i++;
	}
	return (0);
}
